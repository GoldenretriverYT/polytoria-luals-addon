---@meta

---@class CoreUI
--- CoreUI is a static class that allows for the toggling of certain core GUI.
---
--- Only available on the client.
---@field ChatEnabled boolean Determines whether the chat box is visible.
---@field MenuButtonEnabled boolean Determines whether the menu button is visible.
---@field UserCardEnabled boolean Determines whether the user card is visible.
---@field LeaderboardEnabled boolean Determines whether the leaderboard is visible.
---@field HealthBarEnabled boolean Determines whether the health bar is visible.
---@field HotbarEnabled boolean Determines whether the hotbar is visible.
